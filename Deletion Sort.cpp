#include <iostream>
#include <vector>
using namespace std;

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    bool sorted = true;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (i > 0 && a[i] < a[i - 1])
        {
            sorted = false;
        }
    }
    if (sorted)
    {
        cout << n << "\n";
    }
    else
    {
        cout << 1 << "\n";
    }
}
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
}